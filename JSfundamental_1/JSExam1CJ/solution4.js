const day = 7;

switch(day){
    case 1:
        console.log('1-sunday');
        break;
    case 2:
        console.log('2-monday');
        break;
    case 3:
        console.log('3-tuesday');
        break;
    case 4:
        console.log('4-wednesday');
        break;
    case 5:
        console.log('5-thursday');
        break;
    case 6:
        console.log('6-friday');
        break;
    case 7:
        console.log('7-saturday');
        break;
    default:
        console.log('all the seven days a week');
}